class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code here
        // return the head of intersection list
        unordered_map<int,bool>mp;
        Node* ptr1 = head2;
        while(ptr1!=NULL){
            mp[ptr1->data] = true;
            ptr1=ptr1->next;
        }
        ptr1 = head1;
        Node* ans = new Node(-1);
        Node* res = ans;
        while(ptr1!=NULL){
            if(mp[ptr1->data]==true){
                    ans->next = new Node(ptr1->data);
                    ans = ans->next;
            }
            ptr1 = ptr1->next;
        }
        return res->next;
    }
};

